//Variables and Rules
public class VariablesEx {
	public static void main(String arg[]) {
		// Syntax
		// DatatypeName VariableName = Value;
		// Example:
		int a = 10;
		int _1 = 20;
		// VariableNaming Rules
		// 1. Must not Begin With a digit
		// 2. Name is case Sensitive
		// 3. Should not be a keyword
		// 4. White Space is not allowed
		// 5. Can contain alphabets, $character and digits
		// if the other conditions are met.
		// 6. Can Use Underscore also
		System.out.println(_1);

	}
}
